// Arithmetic Operators
console.log(10 + 5); // Addition
console.log(10 - 5); // Subtraction
console.log(10 * 5); // Multiplication
console.log(10 / 5); // Division
console.log(10 % 5); // Modulus
console.log(10 ** 5); // Exponentiation

// Assignment Operators
let x = 10;
x += 5; // x = x + 5
console.log(x); // Output: 15

// Comparison Operators
console.log(10 == 5); // Equal to
console.log(10 != 5); // Not equal to
console.log(10 > 5); // Greater than
console.log(10 < 5); // Less than
console.log(10 >= 5); // Greater than or equal to
console.log(10 <= 5); // Less than or equal to

// Logical Operators
console.log(true && false); // AND
console.log(true || false); // OR
console.log(!true); // NOT

// Bitwise Operators
console.log(5 & 1); // Bitwise AND
console.log(5 | 1); // Bitwise OR
console.log(~5); // Bitwise NOT
console.log(5 ^ 1); // Bitwise XOR
console.log(5 << 1); // Left shift
console.log(5 >> 1); // Right shift
console.log(5 >>> 1); // Unsigned right shift

// String Operators
console.log("Hello" + " " + "World"); // Concatenation

// Conditional (Ternary) Operator
let age = 18;
let status = (age >= 18) ? "adult" : "minor";
console.log(status); // Output: "adult"

// typeof Operator
console.log(typeof "Hello"); // Output: "string"
console.log(typeof 10); // Output: "number"
console.log(typeof true); // Output: "boolean"
console.log(typeof undefined); // Output: "undefined"
console.log(typeof null); // Output: "object"
console.log(typeof {}); // Output: "object"
console.log(typeof []); // Output: "object"
console.log(typeof function(){}); // Output: "function"
